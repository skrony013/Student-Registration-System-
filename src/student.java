    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        // TODO add your handling code here:
        try{
            s = conn.createStatement();
            int stdId = Integer.parseInt(id.getText());
            String sql = "SELECT * FROM STUDENT WHERE id ='"+stdId+"' ";
            r=s.executeQuery(sql);
            if(r.next()){
                name.setText(r.getString("stdName"));
                fatherName.setText(r.getString("stdFatherName"));
                city.setText(r.getString("stdCity"));
                bloodGroup.setText(r.getString("stdBloodGroup"));
                phone.setText(r.getString("stdPhone"));
                stdClass.setText(String.format("%s",r.getInt("class")));
                 
            }else{
                JOptionPane.showMessageDialog(null,"Record Not Found.!!");
            }
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        
    }//GEN-LAST:event_searchActionPerformed

