    private void updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateActionPerformed
        // TODO add your handling code here:
        try{
            s=conn.createStatement();
            int stdId = Integer.parseInt(id.getText());
            String stdname = name.getText();
            String stdBlood = bloodGroup.getText();
            String stdFatherName = fatherName.getText();
            String stdcity = city.getText();

            String stdphone = phone.getText();
            int stdclass = Integer.parseInt(stdClass.getText());

            String sql = "UPDATE STUDENT SET stdName='"+stdname+"',stdFatherName='"+stdFatherName+"',stdBloodGroup='"+stdBlood+"',stdCity='"+stdcity+"',stdPhone='"+stdphone+"',class='"+stdclass+"' WHERE id='"+stdId+"'";
            s.executeUpdate(sql);

            JOptionPane.showMessageDialog(null, "Student information is updated successfully!");
        }catch(SQLException | NumberFormatException e){
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_updateActionPerformed

